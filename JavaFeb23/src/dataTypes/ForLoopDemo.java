package dataTypes;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		/*
		 * for (initialization / start point, how far/end point, how will it increase/decrease )
		 * {
		 * Code to be executed
		 * }
		 */
		
		// write "I love you 10 times using for loop:
		
		System.out.println("Start");

		for(int i =1; i<=10; i++) {
			System.out.println("I love you");
		}
		System.out.println("End");
		
		
		
		System.out.println("Start");
		
		for(int i =1; i<=3; i++) {
			System.out.println(i + ". I am from Brooklyn");
		}
		System.out.println("End");

		// i is the variable chosen for integers here (starting with 1)
		
		// System.out.println ("I am from " +VariableName);
		
		/*
		 * Syntax for While Loop
		 * 
		 * initialization;
		 * while (condition) {
		 * 	//Statement
		 * increment/decrement
		 * } while (condition)
		 * 
		 * 
		 * Syntax for Do While Loop
		 * 
		 * initialization;
		 * do{
		 *	//statement
		 * Increment/decrement
		 * }while (condition)
		 * 
		 * 
		 * 
		 */
		
	}

}
